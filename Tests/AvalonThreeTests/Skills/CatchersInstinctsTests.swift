                coachID: .away,
                payload: .runActionSpecifySquares(
                    playerID: pl(.away, 1),
                    maxRunDistance: 7,
                    validSquares: ValidMoveSquares(
                        intermediate: squares("""
                        aaaaaaaaaaa
                        aaaaaaaaaaa
                        aaaaaaaaaaa
                        a..aaaaa..a
                        a..aaaaa..a
                        ...aaaaaaaa
                        ...aaa.aaaa
                        ...aaaaaaaa
                        aaaaaaaaaaa
                        aaaaaaaaaaa
