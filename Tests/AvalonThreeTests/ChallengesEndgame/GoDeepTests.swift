                        a..aaaaa...
                        a..aaaa....
                        aaaaaaaaa..
                        aaaaaaaaa..
                        aaaaaaaaa..
                        """),
                        final: squares("""
                        ...........
                        ...........
                        ...........
                        ...........
                        ...........
