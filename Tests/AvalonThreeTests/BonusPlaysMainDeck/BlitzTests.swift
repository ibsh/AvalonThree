                        final: squares(
                        """
                        ...........
                        aaaaaaaa...
                        aaaaaaaa...
                        aXXaaaaaXX.
                        aXXaaaaaXX.
                        aaaaaTTT...
                        aaAaaTOT...
                        aaaaaTTT...
                        aaaaaaaa...
                        aaaaaaaa...
                        aXXaaaaaXX.
                        aXXaaaaaXX.
                        ...........
                        ...........
                        ...........
