                    playerID: pl(.away, 0),
                    ballID: nil,
                    from: sq(2, 6),
                    to: sq(1, 6),
                    direction: .west,
                    reason: .followUp
                ),
                .playerFellDown(
                    playerID: pl(.away, 0),
                    in: sq(1, 6),
                    reason: PlayerFallDownReason
                        .divingTackle
                ),
                .discardedActiveBonusPlay(
                    coachID: .away,
                    card: ChallengeCard(
                        challenge: .breakSomeBones,
                        bonusPlay: .divingTackle
                    )
                ),
                .updatedDiscards(
                    top: .divingTackle,
                    count: 1
                ),
            ]
        )

